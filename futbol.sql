DROP TABLE IF EXISTS partidos;
DROP TABLE IF EXISTS plantillas;
DROP TABLE IF EXISTS jugadores;
DROP TABLE IF EXISTS equipos;
DROP TABLE IF EXISTS arbitros;

CREATE TABLE equipos (
	id INT AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(45),
    fecha_fundado DATE
);

CREATE TABLE jugadores (
	id INT AUTO_INCREMENT PRIMARY KEY,
	nombre VARCHAR(45),
    apellido VARCHAR(45),
    nacionalidad VARCHAR(45),
    posicion VARCHAR(45),
    id_equipo INT,
    FOREIGN KEY (id_equipo) REFERENCES equipos(id)
);

CREATE TABLE plantillas (
	id INT AUTO_INCREMENT PRIMARY KEY,
    id_equipo INT,
    id_jugador INT,
    tipo ENUM("titular", "suplente"),
    FOREIGN KEY (id_equipo) REFERENCES equipos(id),
    FOREIGN KEY (id_jugador) REFERENCES jugadores(id)
);

CREATE TABLE arbitros (
	id INT AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(45),
    apellido VARCHAR(45)
);

CREATE TABLE partidos (
	id INT AUTO_INCREMENT PRIMARY KEY,
    id_equipo_local INT,
    id_equipo_visitante INT,
    goles_equipo_local INT,
    goles_equipo_visitante INT,
    id_arbitro INT,
    FOREIGN KEY (id_equipo_local) REFERENCES equipos(id),
    FOREIGN KEY (id_equipo_visitante) REFERENCES equipos(id),
    FOREIGN KEY (id_arbitro) REFERENCES arbitros(id)
);

